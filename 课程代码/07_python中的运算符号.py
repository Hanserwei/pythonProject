# 算术运算符号，+，-,*,/,//,%，**
print("1+1=",1+1)
print("1-1=",1-1)
print("1*1=",1*1)
print("4/2=",4/2)# 结果默认是float类型
print("11//2=",11//2)# 取整除
print("9%2=",9%2)# 取余
print("2**2=",2**2)# 指数运算
# 赋值运算符 =
num = 1+2*3
# 复合赋值运算符 +=，-=,*=,/=,%=,**=,//=
num = 1
num += 1
print(num)
num = 1
num -= 1
print(num)
num = 2
num *= 4
print(num)
num = 3
num %=2
print(num)
num = 3
num **= 2
print(num)
num = 9
num //= 2
print(num)